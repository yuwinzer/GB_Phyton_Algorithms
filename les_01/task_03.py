# 3. По введенным пользователем координатам двух точек вывести уравнение прямой вида y=kx+b, проходящей через эти точки.

x1, y1 = [int(i) for i in input("Введите координаты x и y точки А через пробел: ").split()]
x2, y2 = [int(i) for i in input("Введите координаты x и y точки B через пробел: ").split()]

k = (y1 - y2)/(x1 - x2)
b = y2 - k * x2

print(f'y = {k}x + {b}')
